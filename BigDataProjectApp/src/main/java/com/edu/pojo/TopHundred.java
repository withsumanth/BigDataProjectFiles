package com.edu.pojo;

public class TopHundred {
	String prodId;
	String topVotes;

	public TopHundred() {
		super();
	}

	public String getProdId() {
		return prodId;
	}

	public void setProdId(String prodId) {
		this.prodId = prodId;
	}

	public String getTopVotes() {
		return topVotes;
	}

	public void setTopVotes(String topVotes) {
		this.topVotes = topVotes;
	}

}
